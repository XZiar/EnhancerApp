package xziar.enhancer.pojo;

public class CommentBean
{
	private int tid = -1;
	private int suid = -1;
	private int cuid = -1;
	private int sscore = -1;
	private int cscore = -1;
	private String stoc = "";
	private String ctos = "";

	public int getTid()
	{
		return tid;
	}

	public void setTid(int tid)
	{
		this.tid = tid;
	}

	public int getSuid()
	{
		return suid;
	}

	public void setSuid(int suid)
	{
		this.suid = suid;
	}

	public int getCuid()
	{
		return cuid;
	}

	public void setCuid(int cuid)
	{
		this.cuid = cuid;
	}

	public int getSscore()
	{
		return sscore;
	}

	public void setSscore(int sscore)
	{
		this.sscore = sscore;
	}

	public int getCscore()
	{
		return cscore;
	}

	public void setCscore(int cscore)
	{
		this.cscore = cscore;
	}

	public String getStoc()
	{
		return stoc;
	}

	public void setStoc(String stoc)
	{
		this.stoc = stoc;
	}

	public String getCtos()
	{
		return ctos;
	}

	public void setCtos(String ctos)
	{
		this.ctos = ctos;
	}


}
